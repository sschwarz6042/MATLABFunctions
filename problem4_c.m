function [t,y]=problem4_c()
%Euler

    timeSteps=24000;
    h=17.065211656/timeSteps;

    y0=[0.994, 0, 0, 2.0015851063790825];
    
    y=zeros(24000, 4);
    t=zeros(24000, 1);
    
    u=0.012277471;
    v=1-u;
    
    for i=1:timeSteps
        if(i==1)
            de=((y0(1)+u)^2 + y0(3)^2)^(1/2);
            dm=((y0(1)-v)^2 + y0(3)^2)^(1/2);

            y(i,1)=y0(1)+h*y0(2);
            y(i,2)=y0(2)+h*(y0(1) + 2*y0(4) -v*((y0(1)+u) / (de^3)) -u*((y0(2)-v)/dm^3));
            y(i,3)=y0(3)+h*y0(4);
            y(i,4)=y0(4)+h*(y0(3) - 2*y0(2) -v*(y0(3)/(de^3)) -u*(y(3)/(dm^3)));
        end
        
        if(i>1)
            de=((y(i-1, 1)+u)^2 + y(i-1, 3)^2)^(1/2);
            dm=((y(i-1, 1)-v)^2 + y(i-1, 3)^2)^(1/2);
    
            y(i, 1)=y(i-1, 1) + h*y(i-1, 2);
            y(i, 2)=y(i-1, 2) + h*(y(i-1, 1) + 2*y(i-1, 4) -v*((y(i-1, 1)+u) / (de^3)) -u*((y(i-1, 2)-v)/dm^3));
            y(i, 3)=y(i-1, 3) + h*y(i-1, 4);
            y(i, 4)=y(i-1, 4) + h*(y(i-1, 3) - 2*y(i-1, 2) -v*(y(i-1, 3)/(de^3)) -u*(y(i-1, 3)/(dm^3)));
        end 
        
        t(i, 1)=i*h;
    end    
end